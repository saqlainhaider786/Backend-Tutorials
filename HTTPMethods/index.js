/* 
=> Followings are the http methods : 
i) Get
ii) Post
iii) Put
iv) Patch
v) Delete

Get and Post are mostly used methods.

    ==================================================================

    i) Get : 
            Used when we have to fetch(get) data from the server.
            e.g searching anthing on google or youtube etc.
    ii) Post : 
              Used to send and mutate (change) data in the server.
              e.g forms, sign-in, sign-up etc. 
    iii) Put :
              Used to upload data e.g image,video etc on the server.
    iv) Patch :
               Used to change an existing entry. 
    v)Delete :
              Used to delete an existing entry.
    */